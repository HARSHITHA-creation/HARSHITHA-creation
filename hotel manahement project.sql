create database hotelmanagementsystem;
use hotelmanagementsystem;
CREATE TABLE hotel (
    hotel_id int(5) PRIMARY KEY,  -- Hotel ID, primary key
    hotel_name varchar(100),         -- Hotel name
    address varchar(255),            -- Hotel address
    city varchar(100),               -- City
    state varchar(100),              -- State
    country varchar(100)             -- Country
);
select *from hotel;
CREATE TABLE room (
    room_id int(5) PRIMARY KEY,   -- Room ID, primary key
    hotel_id int(5),              -- Foreign key to hotel table
    room_number varchar(10),        -- Room number
    room_type varchar(50),          -- Room type (e.g., Single, Double, Suite)
    price_pernight DECIMAL(10,2),    -- Price per night
    FOREIGN KEY (hotel_id) REFERENCES hotel(hotel_id)
);
desc room;
desc hotel;
CREATE TABLE guest (
    guest_id int(5) PRIMARY KEY,  -- Guest ID, primary key
    guest_name VARCHAR(100),         -- Guest name
    email VARCHAR(100),              -- Guest email
    phone_number VARCHAR(15)         -- Guest phone number
);
desc guest;
CREATE TABLE booking (
    booking_id int(5) PRIMARY KEY,  -- Booking ID, primary key
    guest_id int(5),                -- Foreign key to guest table
    room_id int(5),                 -- Foreign key to room table
    check_in_date DATE,                -- Check-in date
    check_out_date DATE,               -- Check-out date
    total_amount decimal(10, 2),        -- Total amount for the booking
    FOREIGN KEY (guest_id) REFERENCES guest(guest_id),
    FOREIGN KEY (room_id) REFERENCES room(room_id)
);
desc booking;
CREATE TABLE payment (
    payment_id int(5) PRIMARY KEY,  -- Payment ID, primary key
    booking_id int(5),              -- Foreign key to booking table
    payment_date DATE,                 -- Payment date
    amount_paid decimal(10, 2),         -- Amount paid
    payment_method VARCHAR(50),       -- Payment method (e.g., Credit Card, Cash)
    FOREIGN KEY (booking_id) REFERENCES booking(booking_id)
);
desc payment;
